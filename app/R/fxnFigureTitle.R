#' `fxnFigureTitle.R` - Build title for figure based on user input
#' 
#' @return `figureTitle` - Title for figure based on selected station


fxnFigureTitle <- function() {
  figureTitle <- 
    htmltools::h4(
      htmltools::HTML(
        paste0(
          "Cotton Growth Stages and Heat Units"
        ),
      ),
      
      class = "figure-title"
    )
  
  return(figureTitle)
}
